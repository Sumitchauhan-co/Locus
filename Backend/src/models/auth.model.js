import mongoose from 'mongoose';

const authschema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

const authModel = mongoose.model('Auth', authschema);

export default authModel